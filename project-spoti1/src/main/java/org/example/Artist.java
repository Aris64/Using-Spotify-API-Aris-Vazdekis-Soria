package org.example;

public class Artist {

    String id;
    String name;
    int popularity;
    String genres;
    int followers;

    public Artist(String id, String name, int popularity, String genres, int followers){
        this.id = id;
        this.name = name;
        this.popularity = popularity;
        this.genres = genres;
        this.followers = followers;
    }

}
